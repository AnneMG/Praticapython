#Programa para leitura de duas variaveis e troca de valor entre si

#Entrada
A = int(input('Informe o valor inteiro para a primeira variavel: '))
B = int(input('Informe o valor inteiro para a segunda variavel: '))

#Processamento
aux = A
trocaA = B
trocaB = aux

#Saida
print (f'O valor de A é: {trocaA} e o valor de B é: {trocaB}')